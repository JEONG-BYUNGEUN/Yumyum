package com.yumyum.dto;

public class Customer_couponDTO {

	private String seq;
	private String users_seq;
	private String coupon_seq;
	private String use;
	
	private String rnum;
	private String name;
	private String price;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getUsers_seq() {
		return users_seq;
	}
	public void setUsers_seq(String users_seq) {
		this.users_seq = users_seq;
	}
	public String getCoupon_seq() {
		return coupon_seq;
	}
	public void setCoupon_seq(String coupon_seq) {
		this.coupon_seq = coupon_seq;
	}
	public String getUse() {
		return use;
	}
	public void setUse(String use) {
		this.use = use;
	}
	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}

}
